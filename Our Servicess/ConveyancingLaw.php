           <h1 style="font-family: 'Luckiest Guy'; font-size: 25px; background-color: #000000; color:#ffffff;"> What’s included in the conveyancing process</h1>
           <h style="font-family: 'Luckiest Guy'; font-size: 20px;"> Our conveyancing solicitors will :</h>
            
           <ul>
              <li style="font-family: 'Luckiest Guy'; font-size: 20px; color: #000000">Make sure that the property title is sound and verify the ownership.</li>
              <li style="font-family: 'Luckiest Guy'; font-size: 20px; color: #000000">Check if the property is freehold or leasehold and advise accordingly.</li>
              <li style="font-family: 'Luckiest Guy'; font-size: 20px; color: #000000">Draw up a contract of sale and send it to the buyer’s solicitor.</li>
              <li style="font-family: 'Luckiest Guy'; font-size: 20px; color: #000000">Check for restrictive covenants or rights of way that could affect your plans for the property.</li>
              <li style="font-family: 'Luckiest Guy'; font-size: 20px; color: #000000">Check that buildings and alterations have the correct planning permission, licences and warranties.</li>
              <li style="font-family: 'Luckiest Guy'; font-size: 20px; color: #000000">Check that there are no debts owing against a property, and if there are that they are released before the property is transferred to you.</li>
              <li style="font-family: 'Luckiest Guy'; font-size: 20px; color: #000000">Carry out local authority searches to make sure if there are zoning restrictions or any debts due.</li>
              <li style="font-family: 'Luckiest Guy'; font-size: 20px; color: #000000">Check if there is a lease, and if so, check the lease conditions.</li>
              <li style="font-family: 'Luckiest Guy'; font-size: 20px; color: #000000"> Arrange for additional property searches, depending on the circumstances. 
                  These might including checking previous land use, whether the land is prone to flooding, if there are planned railway or motorway developments, 
                  whether medieval laws still require you to contribute to the local church and so on. </li>
              <li style="font-family: 'Luckiest Guy'; font-size: 20px; color: #000000"> Check mortgage documents. </li>
              <li style="font-family: 'Luckiest Guy'; font-size: 20px; color: #000000"> Ensure the deposit has been paid. </li>
              <li style="font-family: 'Luckiest Guy'; font-size: 20px; color: #000000"> Exchange contracts for you which makes the deal legally binding.</li>
              <li style="font-family: 'Luckiest Guy'; font-size: 20px; color: #000000"> Lodge an interest in the property so that the property cannot be sold to another. </li>
              <li style="font-family: 'Luckiest Guy'; font-size: 20px; color: #000000"> Calculate the final figure to pay. </li>
              <li style="font-family: 'Luckiest Guy'; font-size: 20px; color: #000000"> Arrange for the title to be transferred to the new owner’s name. </li>
              <li style="font-family: 'Luckiest Guy'; font-size: 20px; color: #000000"> Pay Stamp Duty Tax on your behalf. </li>
              <li style="font-family: 'Luckiest Guy'; font-size: 20px; color: #000000"> Lodge the title deeds with the Land Registry. </li>
              <li style="font-family: 'Luckiest Guy'; font-size: 20px; color: #000000"> Send a copy of the deeds to your mortgage lender. </li>
           </ul>
           
