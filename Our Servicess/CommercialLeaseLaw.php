            <h style="font-family: 'Luckiest Guy'; font-size: 20px; color: #000000"> A business property lease or commercial lease is a legally binding contract between a business owner and the owner of the property.

            These documents are a good deal more complicated than residential leases, because the lease is usually heavily customised to the individual situation. 
            The lease terms and conditions need to be very carefully read to make sure that they match the needs of your business, 
            as any mistakes at the beginning could make or break your new business.</h>
           
            <h1 style="font-family: 'Luckiest Guy'; font-size: 25px; background-color: #000000; color:#ffffff;"> Can I negotiate the terms?</h1>
            <h style="font-family: 'Luckiest Guy'; font-size: 20px; color: #000000"> Yes, lease terms are often negotiable. The specific terms or restrictions which may be up for negotiation include the rent amount, 
              rent increases, the duration of the lease, the modifications you can make to the property and whether you can transfer or assign the lease.</h>
           
            <h1 style="font-family: 'Luckiest Guy'; font-size: 25px; background-color: #000000; color:#ffffff;"> What else should I look for? </h1>
            <h style="font-family: 'Luckiest Guy'; font-size: 20px; color: #000000"> This is hard to answer without knowing your specific situation and your business requirements. Some of the common things we find we need to discuss with clients include: </h>
           
                <ul>
              <li style="font-family: 'Luckiest Guy'; font-size: 20px; color: #000000"> If you’re a new business, check that the length of the lease suits you, as many landlords prefer to lock in a longer lease.</li>
              <li style="font-family: 'Luckiest Guy'; font-size: 20px; color: #000000"> There are several ways to calculate rental increases so make sure you understand and agree to the one chosen by your lessor.</li>
              <li style="font-family: 'Luckiest Guy'; font-size: 20px; color: #000000"> Read the section on property improvements very carefully as it should set out who can make changes, who pays for the modifications and whether you have to return the property to its original condition at the end of the lease.</li>
              <li style="font-family: 'Luckiest Guy'; font-size: 20px; color: #000000"> Make sure the lease includes all of the areas you expect to use including bathrooms, parking areas and common areas.</li>
